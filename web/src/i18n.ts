function resolveTemplate(key: string) {
  return (
    translations[currentLanguage]?.[key] ??
    translations.en[key] ??
    key
  );
}

function applyVariables(template: string, vars?: Vars) {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, token) =>
    token in vars ? vars[token] : `{${token}}`
  );
}

export function t(key: string, vars?: Vars): string {
  const template = resolveTemplate(key);
  return applyVariables(template, vars);
}

export function setLanguage(lang: LanguageCode) {
  if (!(lang in SUPPORTED_LANGUAGES) || lang === currentLanguage) return;
  currentLanguage = lang;
  try {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, lang);
  } catch {
    // ignore storage errors
  }
  window.dispatchEvent(new CustomEvent("languagechange"));
}

export function getCurrentLanguage(): LanguageCode {
  return currentLanguage;
}

export function getSupportedLanguages() {
  return Object.entries(SUPPORTED_LANGUAGES) as [LanguageCode, string][];
}

function parseVarsAttribute(value?: string): Vars | undefined {
  if (!value) return undefined;
  try {
    const parsed = JSON.parse(value);
    if (parsed && typeof parsed === "object") {
      return parsed as Vars;
    }
  } catch {
    // ignore invalid JSON
  }
  return undefined;
}

export function applyTranslations(root: ParentNode | null = document) {
  if (!root) return;

  const textNodes = root.querySelectorAll<HTMLElement>("[data-i18n]");
  textNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (!key) return;
    const vars = parseVarsAttribute(node.dataset.i18nVars);
    node.textContent = t(key, vars);
  });

  const htmlNodes = root.querySelectorAll<HTMLElement>("[data-i18n-html]");
  htmlNodes.forEach((node) => {
    const key = node.dataset.i18nHtml;
    if (!key) return;
    const vars = parseVarsAttribute(node.dataset.i18nVars);
    node.innerHTML = t(key, vars);
  });
}
